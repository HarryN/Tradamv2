export function translate(locale: Locale, key: string): string {
  if (!key) return '';
  
  // 1. Exact match check
  const dict = translations[locale];
  if (dict[key]) return dict[key];

  // 2. Normalize key for robust matching
  const normalizedInput = key.toLowerCase().trim()
    .replace(/\s+/g, ' ') // Collapse multiple spaces
    .replace(/[.,\/#!$%\^&\*;:{}=\-_`~()]/g,""); // Remove punctuation

  // 3. Search in current dictionary with same normalization
  const match = Object.keys(dict).find(k => {
    const normalizedKey = k.toLowerCase().trim()
      .replace(/\s+/g, ' ')
      .replace(/[.,\/#!$%\^&\*;:{}=\-_`~()]/g,"");
    return normalizedKey === normalizedInput;
  });
  
  if (match) {
    return dict[match];
  }

  // 4. Fallback to English dictionary (case-insensitive)
  if (locale !== 'en') {
    const enDict = translations.en;
    const enMatch = Object.keys(enDict).find(k => {
      const normalizedKey = k.toLowerCase().trim()
        .replace(/\s+/g, ' ')
        .replace(/[.,\/#!$%\^&\*;:{}=\-_`~()]/g,"");
      return normalizedKey === normalizedInput;
    });
    
    // If we found an English match, use THAT key to look up in the current dictionary
    if (enMatch && dict[enMatch]) return dict[enMatch];
  }

  // 5. Return original key if no match found
  return key;
}
